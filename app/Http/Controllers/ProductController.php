    public function findByIdUser($id)
    {
        try {
            $user = User::findOrFail($id);
            $products = $user->products()->orderBy('name')->paginate(config('app.paginate.product'));

            return view('pages.products.list', [
                'products' => $products,
                'user_name' => $user->name
            ]);
        } catch (Exception $e) {
            return $e->getMessage();
        }
    }
